import 'package:equatable/equatable.dart';
import 'package:meta/meta.dart';

/// A wrapper for email address strings.
///
/// If an email address instance exists, then it has alread been verified.
/// If the string passed into the constructor is invalid, then it will
/// throw an [InvalidEmailAddressException].
@immutable
class EmailAddress extends Equatable {
  final String emailAddress;

  EmailAddress._(this.emailAddress);

  factory EmailAddress(String emailAddress) {
    final isValid = validateEmailAddress(emailAddress);
    if (isValid) {
      return EmailAddress._(emailAddress);
    } else {
      throw InvalidEmailAddressException();
    }
  }

  @override
  List<Object> get props => [emailAddress];
}

bool validateEmailAddress(String emailAddress) {
  final _email = RegExp(
      r"^((([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+(\.([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+)*)|((\x22)((((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(([\x01-\x08\x0b\x0c\x0e-\x1f\x7f]|\x21|[\x23-\x5b]|[\x5d-\x7e]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(\\([\x01-\x09\x0b\x0c\x0d-\x7f]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]))))*(((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(\x22)))@((([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))\.)+(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))$");
  return _email.hasMatch(emailAddress.toLowerCase());
}

class InvalidEmailAddressException implements Exception {
  final String cause;
  InvalidEmailAddressException([this.cause = '']);
}
